// "&&" Comparação se ambas condições retornam verdadeiro (Se isso e aquilo)
// "||" Comparação se uma das condições retorna verdadeiro (Se isso ou aquilo)
// "!" Negação de uma comparação

let a = 5;
let b = 10;

console.log(a === 5 && b === 10);
console.log(a > 5 || b > 5);
console.log(!(a === 5 && b === 10));